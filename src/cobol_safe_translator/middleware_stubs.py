"""Generate middleware interface stubs for translated COBOL projects.

When a COBOL project uses DB2/CICS/DLI/MQ, the translator emits TODO markers.
This module generates typed interface stubs so multiple developers can work
against defined contracts before the middleware is wired up.

Pipeline: ProjectMap -> detect middleware usage -> generate stub files
"""

from __future__ import annotations

from pathlib import Path

from .models import ProjectMap, SoftwareMap


# --- Detection ---

def _detect_middleware(smap: SoftwareMap) -> dict[str, bool]:
    """Detect which middleware a single program uses."""
    program = smap.program
    return {
        "db2": bool(program.sql_blocks),
        "cics": any(
            "CICS" in s.verb.upper()
            for p in program.paragraphs
            for s in p.statements
        ),
        "dli": bool(program.dli_blocks),
        "mq": any(
            "MQPUT" in s.raw_text.upper() or "MQGET" in s.raw_text.upper()
            or "MQOPEN" in s.raw_text.upper() or "MQCLOSE" in s.raw_text.upper()
            for p in program.paragraphs
            for s in p.statements
        ),
    }


def detect_project_middleware(project_map: ProjectMap) -> dict[str, bool]:
    """Detect middleware usage across all programs in a project."""
    combined = {"db2": False, "cics": False, "dli": False, "mq": False}
    for smap in project_map.programs.values():
        per_prog = _detect_middleware(smap)
        for k in combined:
            combined[k] = combined[k] or per_prog[k]
    return combined


# --- Stub templates ---

_DB2_STUB = '''\
"""DB2 database interface stub.

Generated by cobol-safe-translator. Implement these methods to connect
your translated COBOL programs to your database.

Usage:
    from .db2_interface import DB2Connection
    db = DB2Connection()
    db.connect("DSN=MYDB")
"""

from __future__ import annotations

from typing import Any


class DB2Connection:
    """DB-API 2.0 compatible database interface.

    The translated programs call cursor.execute() and cursor.fetchone().
    Implement this class to connect to your actual database (PostgreSQL,
    MySQL, SQLite, etc.).
    """

    def __init__(self) -> None:
        self._connection: Any = None
        self._cursor: Any = None
        self.sqlcode: int = 0

    def connect(self, dsn: str, **kwargs: Any) -> None:
        """Connect to the database.

        Replace with your actual connection logic:
            import psycopg2
            self._connection = psycopg2.connect(dsn)
            self._cursor = self._connection.cursor()
        """
        raise NotImplementedError("Wire up your database connection here")

    def cursor(self) -> Any:
        """Return a DB-API 2.0 cursor."""
        if self._cursor is None:
            raise RuntimeError("Not connected — call connect() first")
        return self._cursor

    def execute(self, sql: str, params: tuple[Any, ...] = ()) -> None:
        """Execute a SQL statement with parameterized values."""
        try:
            self.cursor().execute(sql, params)
            self.sqlcode = 0
        except Exception as exc:
            self.sqlcode = -1
            raise

    def fetchone(self) -> tuple[Any, ...] | None:
        """Fetch one row from the last query."""
        row = self.cursor().fetchone()
        if row is None:
            self.sqlcode = 100  # NOT FOUND
        return row

    def fetchall(self) -> list[tuple[Any, ...]]:
        """Fetch all rows from the last query."""
        return self.cursor().fetchall()

    def commit(self) -> None:
        """Commit the current transaction."""
        if self._connection:
            self._connection.commit()

    def rollback(self) -> None:
        """Roll back the current transaction."""
        if self._connection:
            self._connection.rollback()

    def close(self) -> None:
        """Close the connection."""
        if self._cursor:
            self._cursor.close()
        if self._connection:
            self._connection.close()
'''

