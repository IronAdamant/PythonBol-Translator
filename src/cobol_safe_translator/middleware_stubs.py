# --- Generator ---

def generate_stubs(
    project_map: ProjectMap,
    output_dir: Path,
) -> list[Path]:
    """Generate middleware interface stubs based on detected usage.

    Only generates stubs for middleware actually used by the project.
    Returns list of written file paths.
    """
    usage = detect_project_middleware(project_map)
    written: list[Path] = []
    output_dir.mkdir(parents=True, exist_ok=True)

    if usage["db2"]:
        p = output_dir / "db2_interface.py"
        p.write_text(_DB2_STUB, encoding="utf-8")
        written.append(p)

    if usage["cics"]:
        p = output_dir / "cics_interface.py"
        p.write_text(_CICS_STUB, encoding="utf-8")
        written.append(p)

    if usage["dli"]:
        p = output_dir / "dli_interface.py"
        p.write_text(_DLI_STUB, encoding="utf-8")
        written.append(p)

    if usage["mq"]:
        p = output_dir / "mq_interface.py"
        p.write_text(_MQ_STUB, encoding="utf-8")
        written.append(p)

    return written
